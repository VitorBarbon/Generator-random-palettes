$(document).ready(() => {

  $('#button-menu').on('click', () => {
    if ($('#menu').hasClass('navbar-links-hidden')) {
      $('#menu').removeClass();
      $('#menu').addClass('navbar-links');
      $('#button-menu').html('<i class="fas fa-times"></i>');
      $('#button-menu').css({'font-size': '30px'});
      $('#palettes-content').html('')
    } else if ($('#menu').hasClass('navbar-links')) {
      $('#menu').removeClass();
      $('#menu').addClass('navbar-links-hidden');
      $('#button-menu').html('<i class="fas fa-bars"></i>')
    }
  })

  // -- // 
})